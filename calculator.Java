/**This class contains functions related to basic arithmatic operators
* @since 1.0
* @version 1.0
 */


public class calculator {
    public int sum(int a, int b){
        return a+b;
    }
    public int product(int a,int b){
        return a*b;
    }
    public int diff(int a,int b){
        return a-b;
    }

}